#!/usr/bin/env python3
"""Run or dry-run the local P0 execution plan.

This runner consumes the metadata-only execution plan generated by
`experiments/p0_shards.py execution-plan`. It is intentionally conservative:
existing valid smoke outputs are skipped, missing outputs are executable pending
work, and invalid validation gates fail before any command can mask them.
"""
from __future__ import annotations

import argparse
import csv
import json
import shlex
import subprocess
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Callable

DEFAULT_PLAN = Path("results/latest/p0_shards/execution_plan.json")


@dataclass
class StepValidation:
    valid: bool
    missing_outputs: list[str] = field(default_factory=list)
    errors: list[str] = field(default_factory=list)


@dataclass
class RunSummary:
    total_steps: int
    selected_steps: int
    skipped_steps: int = 0
    pending_steps: int = 0
    executed_steps: int = 0
    dry_run_steps: int = 0
    failed_step: str | None = None
    errors: list[str] = field(default_factory=list)

    @property
    def ok(self) -> bool:
        return self.failed_step is None and not self.errors


CommandRunner = Callable[[str], int]


def load_plan(path: Path = DEFAULT_PLAN) -> dict[str, Any]:
    if not path.exists():
        raise SystemExit(f"Missing execution plan: {path}")
    plan = json.loads(path.read_text())
    if plan.get("paper_allowed") is not False:
        raise SystemExit("execution plan paper_allowed must be false")
    if plan.get("claim_allowed") is not False:
        raise SystemExit("execution plan claim_allowed must be false")
    if not isinstance(plan.get("steps"), list):
        raise SystemExit("execution plan must contain a steps list")
    return plan


def _read_json(path: Path) -> dict[str, Any]:
    if not path.exists():
        raise FileNotFoundError(path)
    payload = json.loads(path.read_text())
    if not isinstance(payload, dict):
        raise ValueError(f"JSON manifest must be an object: {path}")
    return payload


def _csv_row_count(path: Path) -> int:
    if not path.exists():
        raise FileNotFoundError(path)
    with path.open(newline="") as handle:
        return sum(1 for _ in csv.DictReader(handle))


def validate_step_outputs(step: dict[str, Any]) -> StepValidation:
    outputs = step.get("outputs")
    if not isinstance(outputs, dict) or not outputs:
        return StepValidation(valid=False, errors=["step declares no aggregate outputs"])

    validation = step.get("validation")
    if not isinstance(validation, dict):
        return StepValidation(valid=False, errors=["step declares no validation gate"])

    required_outputs = validation.get("required_outputs")
    if not isinstance(required_outputs, list) or not required_outputs:
        return StepValidation(valid=False, errors=["validation.required_outputs is missing"])

    errors: list[str] = []
    missing: list[str] = []
    for output_key in required_outputs:
        if output_key not in outputs:
            errors.append(f"required output key is undeclared: {output_key}")
            continue
        output_path = Path(str(outputs[output_key]))
        if not output_path.exists():
            missing.append(f"{output_key}: {output_path}")

    manifest_path_value = outputs.get("aggregate_manifest")
    manifest: dict[str, Any] | None = None
    if not manifest_path_value:
        errors.append("aggregate_manifest output is required for validation")
    elif Path(str(manifest_path_value)).exists():
        try:
            manifest = _read_json(Path(str(manifest_path_value)))
        except (json.JSONDecodeError, OSError, ValueError) as error:
            errors.append(f"invalid aggregate_manifest: {manifest_path_value}: {error}")
        else:
            if manifest.get("paper_allowed") is not False:
                errors.append(f"aggregate_manifest paper_allowed must be false: {manifest_path_value}")
            if manifest.get("claim_allowed", False) is not False:
                errors.append(f"aggregate_manifest claim_allowed must be false: {manifest_path_value}")
            expected_mode = validation.get("expected_execution_mode")
            mode_matches = not expected_mode or manifest.get("execution_mode") == expected_mode
            if expected_mode and not mode_matches:
                missing.append(
                    "aggregate_manifest execution_mode "
                    f"{manifest.get('execution_mode')!r} != expected {expected_mode!r}: "
                    f"{manifest_path_value}"
                )
            expected_category_count = validation.get("expected_category_count")
            if mode_matches and expected_category_count is not None:
                if manifest.get("category_count") != expected_category_count:
                    errors.append(
                        "aggregate_manifest category_count "
                        f"{manifest.get('category_count')!r} != expected "
                        f"{expected_category_count!r}: {manifest_path_value}"
                    )

    expected_count_field = str(
        validation.get("expected_row_count_field") or "expected_smoke_run_count"
    )
    expected_count = int(step.get(expected_count_field) or 0)
    metrics_path_value = outputs.get("aggregate_metrics")
    if expected_count <= 0:
        errors.append(f"{expected_count_field} must be positive")
    elif not metrics_path_value:
        errors.append("aggregate_metrics output is required for row-count validation")
    elif Path(str(metrics_path_value)).exists() and not missing:
        try:
            actual_count = _csv_row_count(Path(str(metrics_path_value)))
        except (OSError, ValueError) as error:
            errors.append(f"invalid aggregate_metrics: {metrics_path_value}: {error}")
        else:
            if actual_count != expected_count:
                errors.append(
                    f"aggregate_metrics row count {actual_count} != expected {expected_count}: "
                    f"{metrics_path_value}"
                )

    return StepValidation(valid=not errors and not missing, missing_outputs=missing, errors=errors)


def _default_command_runner(command: str) -> int:
    return subprocess.run(shlex.split(command), check=False).returncode


def _selected_steps(
    steps: list[dict[str, Any]],
    *,
    step_selector: str | None,
    start_index: int | None,
    end_index: int | None,
) -> list[tuple[int, dict[str, Any]]]:
    indexed_steps = list(enumerate(steps))
    if step_selector is not None:
        try:
            index = int(step_selector)
        except ValueError:
            matches = [
                (idx, step)
                for idx, step in indexed_steps
                if step.get("step_id") == step_selector
            ]
            if not matches:
                raise SystemExit(f"Unknown step id: {step_selector}")
            return matches
        if index < 0 or index >= len(steps):
            raise SystemExit(f"Step index out of range: {index}")
        return [(index, steps[index])]

    start = 0 if start_index is None else start_index
    end = len(steps) - 1 if end_index is None else end_index
    if start < 0 or end < start or end >= len(steps):
        raise SystemExit(f"Invalid step range: {start}..{end}")
    return indexed_steps[start : end + 1]


def run_execution_plan(
    plan: dict[str, Any],
    *,
    dry_run: bool = False,
    step_selector: str | None = None,
    start_index: int | None = None,
    end_index: int | None = None,
    command_runner: CommandRunner = _default_command_runner,
) -> RunSummary:
    if plan.get("paper_allowed") is not False:
        return RunSummary(0, 0, failed_step="<plan>", errors=["plan paper_allowed must be false"])
    if plan.get("claim_allowed") is not False:
        return RunSummary(0, 0, failed_step="<plan>", errors=["plan claim_allowed must be false"])
    steps = plan.get("steps")
    if not isinstance(steps, list):
        return RunSummary(0, 0, failed_step="<plan>", errors=["plan steps must be a list"])

    selected = _selected_steps(
        steps,
        step_selector=step_selector,
        start_index=start_index,
        end_index=end_index,
    )
    summary = RunSummary(total_steps=len(steps), selected_steps=len(selected))
    for index, step in selected:
        step_id = str(step.get("step_id", index))
        if step.get("paper_allowed") is not False:
            summary.failed_step = step_id
            summary.errors.append("step paper_allowed must be false")
            break
        if step.get("claim_allowed") is not False:
            summary.failed_step = step_id
            summary.errors.append("step claim_allowed must be false")
            break

        validation = validate_step_outputs(step)
        if validation.valid:
            summary.skipped_steps += 1
            print(f"[{index}] SKIP {step_id}")
            continue
        if validation.errors:
            summary.failed_step = step_id
            summary.errors.extend(validation.errors)
            print(f"[{index}] FAIL {step_id}: validation failed")
            break

        summary.pending_steps += 1
        missing = "; ".join(validation.missing_outputs)
        if dry_run:
            summary.dry_run_steps += 1
            print(f"[{index}] DRY-RUN {step_id}: pending ({missing})")
            continue

        command = str(step.get("command", ""))
        if not command:
            summary.failed_step = step_id
            summary.errors.append("step command is missing")
            print(f"[{index}] FAIL {step_id}: missing command")
            break
        print(f"[{index}] RUN {step_id}: {command}")
        return_code = command_runner(command)
        if return_code != 0:
            summary.failed_step = step_id
            summary.errors.append(f"command exited with code {return_code}: {command}")
            print(f"[{index}] FAIL {step_id}: command exited with code {return_code}")
            break
        summary.executed_steps += 1

        post_validation = validate_step_outputs(step)
        if not post_validation.valid:
            summary.failed_step = step_id
            summary.errors.extend(post_validation.missing_outputs)
            summary.errors.extend(post_validation.errors)
            print(f"[{index}] FAIL {step_id}: post-run validation failed")
            break
    return summary


def print_summary(summary: RunSummary) -> None:
    print(
        "summary: "
        f"total={summary.total_steps} selected={summary.selected_steps} "
        f"skipped={summary.skipped_steps} pending={summary.pending_steps} "
        f"executed={summary.executed_steps} dry_run={summary.dry_run_steps}"
    )
    if summary.failed_step:
        print(f"failed_step={summary.failed_step}", file=sys.stderr)
        for error in summary.errors:
            print(f"ERROR: {error}", file=sys.stderr)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--plan", type=Path, default=DEFAULT_PLAN)
    parser.add_argument("--dry-run", action="store_true")
    parser.add_argument("--step", dest="step_selector", help="Run one step by 0-based index or step_id.")
    parser.add_argument("--start-index", type=int)
    parser.add_argument("--end-index", type=int)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    plan = load_plan(args.plan)
    summary = run_execution_plan(
        plan,
        dry_run=args.dry_run,
        step_selector=args.step_selector,
        start_index=args.start_index,
        end_index=args.end_index,
    )
    print_summary(summary)
    if not summary.ok:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
